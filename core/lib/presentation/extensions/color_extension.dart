extension ColorNullableExtension on Color? {
  ColorFilter? asFilter({BlendMode? blendMode}) {
    if (this == null) {
      return null;
    } else {
      return ColorFilter.mode(this!, blendMode ?? BlendMode.srcIn);
    }
  }
}

